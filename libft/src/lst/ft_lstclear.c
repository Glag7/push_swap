#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*node;
	t_list	*next;

	node = *lst;
	while (node)
	{
		next = node->next;
		if (del)
			del(node->content);
		free(node);
		node = next;
	}
	*lst = NULL;
}
